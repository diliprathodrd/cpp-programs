#include <iostream>
using namespace std;

int main() {
    // Variable declaration
    int age;
    float height;
    char grade;

    // Variable initialization
    age = 25;
    height = 5.9;
    grade = 'A';

    // Output the values
    cout << "Age: " << age << endl;
    cout << "Height: " << height << " feet" << endl;
    cout << "Grade: " << grade << endl;

    return 0;
}
